# 생물정보학 및 유전체학을 위한 AI 기반 워크플로우 언어 개발

## 개요

이 저장소는 AI 기반 개발 방법론을 생물정보학 워크플로우 언어 개발에 적용하여 유전체학 파이프라인 생성의 고유한 과제를 해결하는 방법을 탐구합니다.

## 시장 현황

유전체학 분야의 AI 시장은 폭발적인 성장을 보이고 있습니다:
- **2023년**: 7억 3,340만 달러
- **2033년 (예상)**: 353억 달러
- **연평균 성장률**: 47.3%

2025년까지 모든 인간 게놈 데이터를 저장하는 데 약 **40 엑사바이트**가 필요할 것으로 추정됩니다.

## 주요 차이점: 소프트웨어 개발 vs. 생물정보학

### 1. 데이터 복잡성
- 생물정보학은 대규모 이종 생물학적 데이터셋 처리 (테라바이트~페타바이트)
- 단일 전체 게놈 시퀀싱은 약 100GB의 원시 데이터 생성
- 유전체학 시설은 하루에 2,000명 이상 시퀀싱 가능 (일일 약 6TB)

### 2. 계산 요구사항
- 전문화된 고성능 컴퓨팅 인프라
- 워크플로우 관리 시스템 (Nextflow, Snakemake, WDL, CWL)
- 클라우드 네이티브 배포 전략

### 3. 과학적 검증
- 일반적인 소프트웨어 검증을 초과하는 엄격한 재현성 표준
- 출처 추적 요구사항
- 동료 검토 프로세스
- **재현성 위기**: 2009년 연구에서 생물정보학 논문의 11%만 재현 가능

## 현재 워크플로우 언어 생태계

### 시장 리더

| 언어 | GitHub 스타 | 주요 특징 |
|----------|-------------|--------------|
| **Nextflow** | 3,300 | 데이터플로우 지향, DSL-2 모듈성, nf-core 커뮤니티 |
| **Snakemake** | 2,700 | 규칙 기반, Python 통합, 1,602개 워크플로우 |
| **WDL** | - | 버전 1.2.0, 76% 클라우드 채택, Terra/DNANexus |
| **CWL** | - | YAML 기반, 다중 엔진 지원, 표준화 중점 |

### 채택 패턴
- **Nextflow**: nf-core 컬렉션에 94개 저장소
- **Snakemake**: 1,602개 GitHub 저장소 분석됨
- **WDL**: 사용자의 77%가 학계 출신, 76%가 클라우드 환경 사용
- **CWL**: 상호 운용성 및 이식성에 중점

## 워크플로우 개발의 과제

### 1. 레거시 통합
현재 생물정보학 관행은 "원칙적 추상화가 부족하고 재현성을 저해하는 임시 스크립트와 느슨하게 지정된 워크플로우"에 의해 지배되고 있습니다.

### 2. 컨테이너 구성
- Conda 환경은 로컬 설치보다 약 10배 느릴 수 있음
- 성능 vs. 재현성 트레이드오프

### 3. 재현성 위기
- 탐지 과정이 환경 오염에 매우 취약
- 절차적 단계가 종종 LIMS 샘플 데이터와 분리됨
- 완전한 과학적 프로세스 재구성의 어려움

### 4. 학습 곡선
- 전통적인 스크립팅에서 공식적인 워크플로우 시스템으로의 가파른 전환
- 복잡한 다단계 ML 워크플로우: 기록 → 전처리 → 분석 → 시각화

### 5. 데이터 단편화
- 기기 간 표준화 부족
- 독점 데이터 형식이 데이터 사일로 생성
- 호환되지 않는 환경 간 "단편화 세금"

### 6. 성능 제한
- 계산 집약적이고 리소스 요구가 높음
- 워크플로우 관리 오버헤드 (예: 1000개 이상 작업에 대한 Snakemake 메타데이터 처리)

