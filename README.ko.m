- 임상 관련성

## 구현 권장사항

### 단계별 접근법
1. **레벨 0**: 단일 도구 래퍼 (30분-2시간)
2. **레벨 1**: 3-5개 도구를 사용한 단순 분석 파이프라인 (4-8시간)
3. **레벨 2**: 10-20개 도구를 사용한 포괄적 워크플로우 (1-2주)
4. **레벨 3**: 다중 오믹스 통합 파이프라인 (2-6주)
5. **레벨 4**: 엔터프라이즈 규모 유전체학 플랫폼 (2-6개월)

### Agent-as-Code 원칙
- 버전 관리된 Markdown/YAML 아티팩트
- 유전체학 특화 AI 기능의 체계적 관리
- 점진적 채택 및 지속적 개선

### 맥락 엔지니어링
- 유전체학 도메인 전문성을 가진 벡터 데이터베이스
- 생물학적 해석을 위한 RAG
- 문서 샤딩을 통한 90% 토큰 절약
- 100GB 이상의 원시 시퀀싱 데이터 맥락 처리

